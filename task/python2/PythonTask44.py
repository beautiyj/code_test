# 과제.
#        키보드를 통해서 연도(ex) 2020)를  입력 받는다.
#        이때 입력 받은 연도가 윤년인지 평년인지를 판별하는
#        프로그램을 작성하세요?

#     // 1년 365.242374
#     // 평년 = 365일 (2월달 - 28일까지)
#     // 윤년 = 366일 (2월달 - 29일까지)

#     * 윤년의 정의

#     1. 해당 연도를 4로 나누어 떨어지면 윤년
#     2. 그 중에서 100으로 나누어 떨어지면 윤년이 아님
#     3. 그 중에서 400으로 나누어 떨어지면 윤년

def main():
    print("연도를 입력하세요?")
    # 파이썬은 input()으로 받은 문자열을 int()로 직접 형변환함
    try:
        y = int(input())

        # 파이썬은 && 대신 and, || 대신 or 를 사용함
        if (y % 4 == 0 and y % 100 != 0) or y % 400 == 0:
            print(f"{y}은 윤년")
        else:
            print(f"{y}은 평년")
    except ValueError:
        print("숫자만 입력 가능합니다.")

if __name__ == "__main__":
    main()