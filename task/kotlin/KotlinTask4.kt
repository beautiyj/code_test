/*
* 4. 구구단 2~9단을 세로 방향으로 출력하는 프로그램
*
* <예시> [2단] [3단] [4단] ... [9단]
* 2*1=2 3*1=3 ...
* 2*2=4
* ...
* 2*9=18 3*9=27 ...
*
* 예시처럼 열 방향으로 진행된다.
*/

fun main() {
    for (n in 2..9) {
        print("[${n}단]\t")
    }
    println()

    for (i in 1..9) {
        for (j in 2..9) {
            print("${j}*${i} = ${i * j}\t")
//            print("%d*%d=%2d\t".format(j, i, i * j))
        }
        println()
    }
}

